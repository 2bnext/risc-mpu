// ssd1306.h — SSD1306 128x64 OLED driver over I2C (address 0x3C).
//
// Provides: ssd_init, ssd_clear, ssd_setpos, ssd_putchar, ssd_puts,
//           ssd_putint, init_font.
// Requires: i2c_start, i2c_stop, i2c_write from stdlib.

#define SSD1306_ADDR 0x3C
#define SSD1306_WRITE 0x78
#define SSD1306_CMD   0x00
#define SSD1306_DATA  0x40

// ---- Font (5x8, stored as 5 bytes per ASCII char) ----

char font[640];

void fset(int ch, int b0, int b1, int b2, int b3, int b4) {
    int i = ch * 5;
    font[i]   = b0; font[i+1] = b1; font[i+2] = b2;
    font[i+3] = b3; font[i+4] = b4;
}

