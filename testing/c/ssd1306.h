// ---- SSD1306 driver ----

void ssd_cmd(int c) {
    i2c_start();
    i2c_write(SSD1306_WRITE);
    i2c_write(SSD1306_CMD);
    i2c_write(c);
    i2c_stop();
}

void ssd_init() {
    ssd_cmd(0xAE);
    ssd_cmd(0xD5); ssd_cmd(0x80);
    ssd_cmd(0xA8); ssd_cmd(0x3F);
    ssd_cmd(0xD3); ssd_cmd(0x00);
    ssd_cmd(0x40);
    ssd_cmd(0x8D); ssd_cmd(0x14);
    ssd_cmd(0x20); ssd_cmd(0x02);
    ssd_cmd(0xA1);
    ssd_cmd(0xC8);
    ssd_cmd(0xDA); ssd_cmd(0x12);
    ssd_cmd(0x81); ssd_cmd(0xCF);
    ssd_cmd(0xD9); ssd_cmd(0xF1);
    ssd_cmd(0xDB); ssd_cmd(0x40);
    ssd_cmd(0xA4);
    ssd_cmd(0xA6);
    ssd_cmd(0xAF);
    init_font();
}

void ssd_setpos(int col, int page) {
    ssd_cmd(0xB0 + page);
    ssd_cmd(col & 0x0F);
    ssd_cmd(0x10 + (col >> 4));
}

void ssd_clear() {
    int p;
    int i;
    for (p = 0; p < 8; p = p + 1) {
        ssd_setpos(0, p);
        i2c_start();
        i2c_write(SSD1306_WRITE);
        i2c_write(SSD1306_DATA);
        for (i = 0; i < 128; i = i + 1)
            i2c_write(0x00);
        i2c_stop();
    }
}

void ssd_putchar(int ch) {
    int i;
    int base;
    base = ch * 5;
    i2c_start();
    i2c_write(SSD1306_WRITE);
    i2c_write(SSD1306_DATA);
    for (i = 0; i < 5; i = i + 1)
        i2c_write(font[base + i]);
    i2c_write(0x00);
    i2c_stop();
}

void ssd_puts(char *s) {
    int i;
    i = 0;
    for (;;) {
        if (s[i] == 0) return;
        ssd_putchar(s[i]);
        i = i + 1;
    }
}

void ssd_putint(int v) {
    char buf[12];
    int i;
    int neg;
    neg = 0;
    if (v < 0) { neg = 1; v = 0 - v; }
    i = 0;
    if (v == 0) { buf[0] = '0'; i = 1; }
    for (;;) {
        if (v == 0) break;
        buf[i] = '0' + (v - (v / 10) * 10);
        v = v / 10;
        i = i + 1;
    }
    if (neg) { buf[i] = '-'; i = i + 1; }
    for (;;) {
        if (i == 0) break;
        i = i - 1;
        ssd_putchar(buf[i]);
    }
}

